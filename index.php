        <h2>Secure Network Access Control for Kids</h2>
        <p class="lead">SNACK est une solution complète permettant à un administrateur réseau de gérer le contrôle d'accès au réseau de son entreprise. À l'aide du protocole 802.1X et du serveur Radius, tous les accès au réseau sont contrôlés et toute l'activité est enregistrée. <br />
        SNACK gère également la sauvegarde automatique et la restauration des configurations des équipements réseau, ainsi que la supervision d'un réseau.<br />
        Ces fonctionnalités sont proposées dans une interface web simple, sécurisée et intuitive.
